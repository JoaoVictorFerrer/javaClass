public class SentenciaIfElse {

    public static void main(String[] args) {
        int num = 1;
        if (num == 1 ) {
            System.out.println("bien");
        }else if (num < 1 ) {
            System.out.println("mal");
        }else {
            System.out.println("MUY MAL");
        }
    }
}
